-- soal1
SELECT m.NAMA,
       TRUNC(MONTHS_BETWEEN(SYSDATE, m.TGL_LAHIR) / 12) AS UMUR
FROM MEMBER_103122400041 m
WHERE m.ID_MEMBER IN (
    SELECT tr.ID_MEMBER
    FROM TRANSAKSI_103122400041 tr
    JOIN JADWAL_103122400041 j ON tr.ID_JADWAL = j.ID_JADWAL
    JOIN FILM_103122400041 f ON j.ID_FILM = f.ID_FILM
    JOIN INVENTORI_103122400041 i ON tr.ID_INVENTORI = i.ID_INVENTORI
    WHERE f.JUDUL = 'Habibie Ainun 3'
    AND i.ID_THEATER IN (
        SELECT ID_THEATER FROM THEATER_103122400041
        WHERE HARGA > (
            SELECT HARGA FROM THEATER_103122400041
            WHERE ID_THEATER = 'Teater 1'
        )
    )
);

-- soal2
SELECT f.JUDUL AS JUDUL_FILM,
       t.ID_THEATER AS NOMOR_TEATER,
       t.HARGA
FROM FILM_103122400041 f
JOIN JADWAL_103122400041 j ON f.ID_FILM = j.ID_FILM
JOIN THEATER_103122400041 t ON j.ID_THEATER = t.ID_THEATER
WHERE t.HARGA = (SELECT MIN(HARGA) FROM THEATER_103122400041)
   OR t.HARGA = (SELECT MAX(HARGA) FROM THEATER_103122400041)
ORDER BY t.HARGA;

-- soal3
SELECT f.JUDUL AS JUDUL_FILM,
       COUNT(j.ID_JADWAL) AS JUMLAH_TAYANG
FROM FILM_103122400041 f
JOIN JADWAL_103122400041 j ON f.ID_FILM = j.ID_FILM
GROUP BY f.JUDUL
HAVING COUNT(j.ID_JADWAL) > (
    SELECT MIN(cnt) FROM (
        SELECT COUNT(ID_JADWAL) AS cnt
        FROM JADWAL_103122400041
        GROUP BY ID_FILM
    )
);

-- soal4
SELECT j.ID_THEATER AS NOMOR_TEATER,
       f.JUDUL AS JUDUL_FILM,
       COUNT(j.ID_JADWAL) AS JUMLAH_TAYANG
FROM FILM_103122400041 f
JOIN JADWAL_103122400041 j ON f.ID_FILM = j.ID_FILM
WHERE f.DURASI > 100
GROUP BY j.ID_THEATER, f.JUDUL
HAVING COUNT(j.ID_JADWAL) > (
    SELECT AVG(cnt) FROM (
        SELECT COUNT(ID_JADWAL) AS cnt
        FROM JADWAL_103122400041
        GROUP BY ID_THEATER, ID_FILM
    )
);

