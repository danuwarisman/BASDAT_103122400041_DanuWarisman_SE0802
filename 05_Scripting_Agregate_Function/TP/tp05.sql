SELECT m.ID_MEMBER, m.NAMA, COUNT(t.ID_JADWAL) AS JUMLAH_TONTON
FROM MEMBER2 m
LEFT JOIN TRANSAKSI2 t ON m.ID_MEMBER = t.ID_MEMBER
WHERE m.TANGGAL_LAHIR = (
    SELECT MAX(TANGGAL_LAHIR) FROM MEMBER2
)
GROUP BY m.ID_MEMBER, m.NAMA;

SELECT 
    TO_CHAR(TANGGAL_LAHIR, 'MM') AS BULAN,
    COUNT(*) AS JUMLAH_MEMBER
FROM MEMBER2
GROUP BY TO_CHAR(TANGGAL_LAHIR, 'MM')
ORDER BY BULAN;

