CREATE VIEW V_JADWAL_FILM AS
SELECT 
    f.JUDUL,
    j.ID_THEATER,
    j.JAM_TAYANG,
    j.PERIODE_START,
    j.PERIODE_END
FROM FILM2 f
JOIN JADWAL2 j ON f.ID_FILM = j.ID_FILM;

CREATE MATERIALIZED VIEW MV_JADWAL_FILM
AS
SELECT 
    f.JUDUL,
    j.ID_THEATER,
    j.JAM_TAYANG,
    j.PERIODE_START,
    j.PERIODE_END
FROM FILM2 f
JOIN JADWAL2 j ON f.ID_FILM = j.ID_FILM;







